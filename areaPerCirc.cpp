// This program calculates the area and perimeter of a circle.

#include <cmath>
#include <iostream>

int main() {
    // Radius
    double rad = 10;

    // Process
    int circ = M_PI * (2 * rad);
    int area = M_PI * (rad * rad);

    // Output
    std::cout << circ << " " << area << std::endl;
    std::cout << sqrt(100) << std::endl;
    std::cout << sqrt(25) << std::endl;

    return 0;
}
